
# range(10)
r1 = range(10)
print("range(10)", r1)

#range(start, stop, step): - 시작에 0과 스텝에 1은 default 값으로 생략가능 
# range(0, 10, 1) <- 0부터 10 전까지 = 9까지, 1씩 올리면서) 
r2 = range(0, 10, 1)
print("range(0, 10, 1)", r2)


# range(0, 10, 2)
r3 = range(0, 10, 2)
print("range(0, 10, 2)", r3)


# range(0, 10, 1)
r4 = range(10, 0, -1)
print("range(10, 0, -1)", r4)
